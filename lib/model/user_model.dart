class User {
  final String name;
  final String image;
  final int metres;
  final int minutes;
  final int calories;
  final int steps;
  final int metresPerMinute;

  User({
    required this.name,
    required this.image,
    required this.metres,
    required this.minutes,
    required this.calories,
    required this.steps,
    required this.metresPerMinute,
  });
}
